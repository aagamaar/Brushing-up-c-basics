#include <stdio.h>
int main()
{
    // Arithmetic Operators
    int a = 10, b = 8, c = 9;
    printf("The sum of %d and %d = %d\n", a, b, a + b);
    printf("The difference of %d and %d = %d\n", a, b, a - b);
    printf("The product of %d and %d = %d\n", a, b, a * b);
    printf("The quotient of %d and %d = %d\n", a, b, a/b);
    printf("The modulo division of %d and %d = %d\n", a, b, a%b);
    printf("The increment result of a = %d\n",++a);
    printf("The decrement result of a = %d",--a);
    

    return 0;
}